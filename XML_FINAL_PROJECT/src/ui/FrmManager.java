    private String buildingSQLSelectToBindTable(String[] inArr, String tableName) {
        String ret = "select";
        for (String string : inArr) {
            ret += " " + string + ",";
        }
        ret = ret.substring(0, ret.length() - 1) + " from " + tableName;
        return ret;
    }

