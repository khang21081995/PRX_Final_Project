    private int btnShow(int caze) {
        switch (caze) {
            case 2:
            case 0:
                btnAdd.setVisible(true);
                btnUpdate.setVisible(true);
                btnBlock.setVisible(true);
                if (caze == 0) {
                    btnBlock.setVisible(false);
                }
                break;
            case 1:
                btnAdd.setVisible(false);
                btnUpdate.setVisible(false);
                btnBlock.setVisible(false);
                break;

        }
        return caze;
    }

